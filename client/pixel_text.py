class PixelTextRenderer:
    def __init__(self, pixel_size: int = 2, letter_spacing: int = 1) -> None:
        self.pixel_size = pixel_size
        self.letter_spacing = letter_spacing

    def draw(
        self,
        surface: pygame.Surface,
        text: str,
        position: tuple[int, int],
        color: tuple[int, int, int],
        scale: int = 1,
    ) -> None:
        x, y = position
        row_x = x

        for character in text.upper():
            if character == "\n":
                y += (7 * self.pixel_size * scale) + (2 * scale)
                row_x = x
                continue

            glyph = GLYPHS.get(character, GLYPHS["?"])
            self._draw_glyph(surface, glyph, row_x, y, color, scale)
            row_x += ((len(glyph[0]) + self.letter_spacing) * self.pixel_size * scale)

    def measure(self, text: str, scale: int = 1) -> tuple[int, int]:
        width = 0
        max_width = 0
        height = 7 * self.pixel_size * scale

        for character in text.upper():
            if character == "\n":
                max_width = max(max_width, width)
                width = 0
                height += (7 * self.pixel_size * scale) + (2 * scale)
                continue

            glyph = GLYPHS.get(character, GLYPHS["?"])
            width += ((len(glyph[0]) + self.letter_spacing) * self.pixel_size * scale)

        max_width = max(max_width, width)
        return (max_width, height)

    def _draw_glyph(
        self,
        surface: pygame.Surface,
        glyph: Glyph,
        x: int,
        y: int,
        color: tuple[int, int, int],
        scale: int,
    ) -> None:
        pixel = self.pixel_size * scale
        for row_index, row in enumerate(glyph):
            for column_index, bit in enumerate(row):
                if bit != "1":
                    continue
                rect = pygame.Rect(
                    x + (column_index * pixel),
                    y + (row_index * pixel),
                    pixel,
                    pixel,
                )
                pygame.draw.rect(surface, color, rect)
